export default class Calculator {
  sum(val1: any, val2: number): number {
    return val1 + val2;
  }

  sub(val1: any, val2: number): number {
    return val1 - val2;
  }

  mult(val1: any, val2: number): number {
    return val1 * val2;
  }

  div(val1: any, val2: number): number {
    return val1 / val2;
  }
}
